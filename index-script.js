let popup = document.getElementById("popup-cara"),
    popup2 = document.getElementById("popup-tentang");

function openPopupCara(){
        popup.classList.add("open-popup-cara");
}
    
function closePopupCara(){
        popup.classList.remove("open-popup-cara");
}
    

function openPopupTentang(){
    popup2.classList.add("open-popup-tentang");
}

function closePopupTentang(){
    popup2.classList.remove("open-popup-tentang");
}
